-- V2: Dictionary table for application user roles
-- (Administrator, Osoba planująca, Osoba nadzorująca, Pilot)

CREATE SEQUENCE FRU_DICT_USER_ROLES_SEQ START WITH 1 INCREMENT BY 1 NOCACHE;

CREATE TABLE FRU_DICT_USER_ROLES (
    id   NUMBER        DEFAULT FRU_DICT_USER_ROLES_SEQ.NEXTVAL NOT NULL,
    name VARCHAR2(100) NOT NULL,
    CONSTRAINT FRU_DICT_USER_ROLES_PK PRIMARY KEY (id),
    CONSTRAINT FRU_DICT_USER_ROLES_UK UNIQUE (name)
);
